import fs from 'node:fs/promises';
import path from 'node:path';
import imageToAscii from 'image-to-ascii';

const root = process.cwd();
const source = path.join(root, 'resources/images/logokabinet.png');
const output = path.join(root, 'resources/svelte/lib/logokabinet-ascii.js');
const characters = ' .,:;irsXA253hMHGS#9B&@';

const escapeHtml = (value) => value
  .replaceAll('&', '&amp;')
  .replaceAll('<', '&lt;')
  .replaceAll('>', '&gt;')
  .replaceAll('"', '&quot;')
  .replaceAll("'", '&#39;');

const brightenChannel = (value) => Math.max(0, Math.min(255, Math.round((value * 1.22) + 14)));

const matrixToHtml = (matrix) => matrix.map((row) => row.map((cell) => {
  const char = cell?.char || ' ';
  const pixel = cell?.pixel;

  if (char === ' ' || !pixel) {
    return ' ';
  }

  const r = brightenChannel(pixel.r);
  const g = brightenChannel(pixel.g);
  const b = brightenChannel(pixel.b);

  return `<span style="color:rgb(${r},${g},${b})">${escapeHtml(char)}</span>`;
}).join('')).join('\n');

const ascii = await new Promise((resolve, reject) => {
  imageToAscii(
    source,
    {
      colored: false,
      white_bg: false,
      pixels: characters,
      pxWidth: 1.25,
      size: { width: 64 },
      stringify: false,
      concat: false,
    },
    (error, converted) => {
      if (error) {
        reject(error);
        return;
      }

      resolve(matrixToHtml(converted).trimEnd());
    },
  );
});

const fileContents = `// Generated by scripts/generate-logokabinet-ascii.mjs using image-to-ascii.\nexport const logokabinetAscii = ${JSON.stringify(ascii)};\n`;

await fs.writeFile(output, fileContents, 'utf8');
